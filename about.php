    <h2 class="content-part">What is File Buy?</h2>
    <p class="content-part">
      Freelancers often sell their art through insecure methods. Either the seller sends the file before accepting payment, or the buyer pays before receiving the file. This leaves room for one party to scam another, either by accepting payment and abandoning the project or by accepting the finished work and not paying.
    </p>
    <p class="content-part">
      File Buy resolves this problem by acting as a middle party between the buyer and seller.  To list a file for sale, the seller simply uploads their file, receives a link to the listing, and sends that link to their buyer.  Once the buyer has paid for the file, it becomes available for download. Before purchasing, the buyer only sees a preview of the file, which is a copy of the image at 5% of its original quality.
    </p>
    <p class="content-part">
      Another risk of selling art online occurs when a buyer makes a request for a commission and decides against purchasing the piece after the artist the artist has neared completion. To prevent this, File Buy also provides a commission system, which splits the purchasing of files into multiple “milestones”. The seller can decide how many milestones the project should contain, and assign a price to each milestone. The seller uploads a file for each milestone. After they have been paid, the next step becomes available. This way, artists can be sure they’re adequately compensated as they work on the project.
    </p>
    <h2 class="content-part">How much does it cost?</h2>
    <p class="content-part">
